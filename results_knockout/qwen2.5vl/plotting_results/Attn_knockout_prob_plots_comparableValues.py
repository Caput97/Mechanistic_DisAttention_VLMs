def compute_masked_minus_none_matrix(
    summary: pd.DataFrame,
    value_col: str,
    condition: str,          # "caption" or "foil"
    none_key: str = "none",
) -> pd.DataFrame:
    """
    Build (mask setting excluding 'none') x layer matrix with delta = masked - none
    for a given condition ("caption" or "foil").
    """
    subset = summary[summary["condition"] == condition].copy()
    if subset.empty:
        return pd.DataFrame()

    wide = subset.pivot_table(
        index="token_slot",
        columns="layer",
        values=value_col,
        aggfunc="mean",
    ).sort_index(axis=1)

    if none_key not in wide.index:
        return pd.DataFrame()

    baseline = wide.loc[none_key]
    masked = wide.drop(index=[none_key])

    delta_df = masked.sub(baseline, axis=1)
    delta_df = delta_df.loc[sorted(delta_df.index, key=_token_sort_key)].sort_index(axis=1)
    return delta_df


def plot_heatmap_fixed_scale(
    delta_df: pd.DataFrame,
    out_path: Path,
    vmax: float,
    label: str,
    title: str,
    y_label: str,
) -> None:
    """Generic heatmap with fixed scale [-vmax, +vmax]."""
    if delta_df is None or delta_df.empty:
        print(f"[SKIP] Empty matrix: {out_path}")
        return

    fig, ax = plt.subplots(figsize=(12, max(4, 0.25 * len(delta_df))))

    im = ax.imshow(
        delta_df,
        aspect="auto",
        cmap="coolwarm",
        vmin=-vmax,
        vmax=vmax,
    )

    ax.set_xticks(range(len(delta_df.columns)))
    ax.set_xticklabels(delta_df.columns, rotation=90)
    ax.set_yticks(range(len(delta_df.index)))
    ax.set_yticklabels(delta_df.index)

    ax.set_xlabel("Indice layer")
    ax.set_ylabel(y_label)
    ax.set_title(title)

    cbar = fig.colorbar(im, ax=ax, shrink=0.6)
    cbar.set_label(f"Δ {label}")

    fig.tight_layout()
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(out_path, dpi=200)
    plt.close(fig)
    print(f"Saved: {out_path}")


def _prepare_line_data(summary: pd.DataFrame, condition: str, value_col: str) -> Optional[pd.DataFrame]:
    """
    Prepare layer x base_token table for a given condition ("caption"/"foil")
    collapsing token_slot -> base_token (mean).
    """
    subset = summary[summary["condition"] == condition].copy()
    if subset.empty:
        return None

    subset["base_token"] = subset["token_slot"].map(_make_base_token)

    grouped = (
        subset.groupby(["layer", "base_token"])[value_col]
        .mean()
        .reset_index()
    )

    line_df = grouped.pivot(index="layer", columns="base_token", values=value_col)

    preferred = [c for c in line_df.columns if c in PREFERRED_TOKEN_ORDER]
    others = sorted([c for c in line_df.columns if c not in PREFERRED_TOKEN_ORDER])

    preferred = sorted(preferred, key=lambda x: PREFERRED_TOKEN_ORDER_MAP.get(x, len(PREFERRED_TOKEN_ORDER)))
    line_df = line_df[preferred + others].sort_index()
    return line_df


def plot_caption_foil_trends_pdf(
    summary: pd.DataFrame,
    out_path: Path,
    value_col: str,
    label: str,
    title_prefix: str,
) -> None:
    """PDF 1×2: per-layer trends for caption and foil (one line per base token)."""
    fig, axes = plt.subplots(nrows=1, ncols=2, figsize=(16, 6), sharex=True, sharey=False)

    for ax, condition in zip(axes, ["caption", "foil"]):
        line_df = _prepare_line_data(summary, condition=condition, value_col=value_col)
        if line_df is None or line_df.empty:
            ax.set_visible(False)
            continue

        for token_name in line_df.columns:
            ax.plot(line_df.index, line_df[token_name], marker="o", label=token_name)

        ax.set_xlabel("Indice layer")
        ax.set_ylabel(label)
        ax.set_title(f"{title_prefix} — {condition}")

    if axes[0].get_visible():
        axes[0].legend(loc="best", fontsize=8)

    fig.tight_layout()
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(out_path, dpi=200)
    plt.close(fig)
    print(f"Saved: {out_path}")

