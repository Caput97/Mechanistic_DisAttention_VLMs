def subdir_name_from_file(path: Path) -> str:
    stem = path.stem
    parts = stem.split("_")
    # esempio: prendo gli ultimi 2 pezzi -> "..._EN_spatial" oppure "..._ITA_temporal"
    return "_".join(parts[-2:])  # EN_spatial, EN_temporal, ITA_spatial, ITA_temporal


def _abs_values_from_df(df: pd.DataFrame) -> np.ndarray:
    """Flatten abs values, keep finite only."""
    v = np.abs(df.values)
    v = v[np.isfinite(v)]
    return v


def _clipping_stats_from_vals(abs_vals: np.ndarray, vmax: float) -> Tuple[int, int, float, float]:
    """Return (n_points, n_clipped, perc_clipped, max_abs)."""
    n_total = int(abs_vals.size)
    if n_total == 0:
        return (0, 0, 0.0, 0.0)
    n_clipped = int((abs_vals > vmax).sum())
    perc = 100.0 * n_clipped / n_total
    max_abs = float(abs_vals.max())
    return (n_total, n_clipped, perc, max_abs)


def main() -> None:
    args = parse_args()
    args.outdir.mkdir(parents=True, exist_ok=True)

    # Load + summarize each file
    per_file: Dict[Path, Dict[str, object]] = {}
    for in_path in args.input:
        df = load_results_jsonl(in_path)
        print(f"[{in_path.name}] Loaded rows: {len(df)}")
        summary = summarize_overall(df)
        per_file[in_path] = {"df": df, "summary": summary}

    value_col = "mean_logprob" if args.value_type == "logprob" else "mean_prob"
    ylabel = "log-probability" if args.value_type == "logprob" else "probability"

    # Compute matrices per file + collect abs values for global percentiles
    abs_cf_list: List[np.ndarray] = []
    abs_mvn_list: List[np.ndarray] = []

    for in_path, blob in per_file.items():
        summary: pd.DataFrame = blob["summary"]  # type: ignore

        # Δ(caption - foil)
        cf = compute_caption_minus_foil_matrix(summary, value_col=value_col)
        blob["delta_caption_minus_foil"] = cf
        v_cf = _abs_values_from_df(cf) if not cf.empty else np.array([], dtype=float)
        if v_cf.size:
            abs_cf_list.append(v_cf)

        # Δ(masked - none): caption and foil (if baseline exists)
        mvn_caption = compute_masked_minus_none_matrix(
            summary, value_col=value_col, condition="caption", none_key=args.none_key
        )
        mvn_foil = compute_masked_minus_none_matrix(
            summary, value_col=value_col, condition="foil", none_key=args.none_key
        )
        blob["delta_masked_minus_none_caption"] = mvn_caption
        blob["delta_masked_minus_none_foil"] = mvn_foil

        v_mc = _abs_values_from_df(mvn_caption) if not mvn_caption.empty else np.array([], dtype=float)
        v_mf = _abs_values_from_df(mvn_foil) if not mvn_foil.empty else np.array([], dtype=float)
        if v_mc.size:
            abs_mvn_list.append(v_mc)
        if v_mf.size:
            abs_mvn_list.append(v_mf)

    if not abs_cf_list:
        print("No valid Δ(caption-foil) matrices found (missing caption/foil?). Exiting.")
        return

    # Global vmax for Δ(caption - foil)
    global_abs_cf = np.concatenate(abs_cf_list)
    vmax_cf = float(np.percentile(global_abs_cf, args.percentile))
    if not np.isfinite(vmax_cf) or vmax_cf <= 0:
        vmax_cf = float(np.nanmax(global_abs_cf)) if global_abs_cf.size else 1.0
        vmax_cf = vmax_cf if np.isfinite(vmax_cf) and vmax_cf > 0 else 1.0

    # Global vmax for Δ(masked - none) (may be absent if 'none' missing)
    vmax_mvn: Optional[float] = None
    if abs_mvn_list:
        global_abs_mvn = np.concatenate(abs_mvn_list)
        vmax_mvn = float(np.percentile(global_abs_mvn, args.percentile))
        if not np.isfinite(vmax_mvn) or vmax_mvn <= 0:
            vmax_mvn = float(np.nanmax(global_abs_mvn)) if global_abs_mvn.size else 1.0
            vmax_mvn = vmax_mvn if np.isfinite(vmax_mvn) and vmax_mvn > 0 else 1.0

    print("\n=== Global comparable scales ===")
    print(f"Value type: {args.value_type} ({value_col})")
    print(f"Percentile: p{args.percentile:g}")
    print(f"vmax caption-foil: {vmax_cf:.6f} -> [{-vmax_cf:.6f}, +{vmax_cf:.6f}]")
    if vmax_mvn is not None:
        print(f"vmax masked-none: {vmax_mvn:.6f} -> [{-vmax_mvn:.6f}, +{vmax_mvn:.6f}]")
    else:
        print(f"vmax masked-none: NOT computed (baseline '{args.none_key}' missing or no data).")
    print()

    # Save scale info to root outdir
    scale_info = {
        "value_type": args.value_type,
        "value_column": value_col,
        "percentile": args.percentile,
        "none_key": args.none_key,
        "scales": {
            "caption_minus_foil": {"vmin": -vmax_cf, "vmax": vmax_cf},
            "masked_minus_none": (
                {"vmin": -vmax_mvn, "vmax": vmax_mvn} if vmax_mvn is not None else None
            ),
        },
        "n_files": len(args.input),
        "input_files": [str(p.resolve()) for p in args.input],
        "cli_command": " ".join(sys.argv),
    }

    scale_info_path = args.outdir / "scale_info.json"
    with scale_info_path.open("w", encoding="utf-8") as f:
        json.dump(scale_info, f, indent=2)
    print(f"Saved scale info: {scale_info_path}\n")

    # Per-file plots + clipping report
    report_rows: List[Dict[str, object]] = []

    for in_path, blob in per_file.items():
        summary: pd.DataFrame = blob["summary"]  # type: ignore
        cf: pd.DataFrame = blob.get("delta_caption_minus_foil", pd.DataFrame())  # type: ignore
        mvn_caption: pd.DataFrame = blob.get("delta_masked_minus_none_caption", pd.DataFrame())  # type: ignore
        mvn_foil: pd.DataFrame = blob.get("delta_masked_minus_none_foil", pd.DataFrame())  # type: ignore

        subdir = args.outdir / subdir_name_from_file(in_path)
        subdir.mkdir(parents=True, exist_ok=True)

        # Save summary CSV
        summary_path = subdir / "knockout_summary_overall.csv"
        summary.to_csv(summary_path, index=False)

        # --- Plot Δ(caption - foil)
        plot_heatmap_fixed_scale(
            delta_df=cf,
            out_path=subdir / "delta_caption_minus_foil.png",
            vmax=vmax_cf,
            label=ylabel,
            title=f"Δ {ylabel} (caption - foil) — {in_path.name}",
            y_label="Componente / token slot",
        )
        plot_heatmap_fixed_scale(
            delta_df=cf,
            out_path=subdir / "delta_caption_minus_foil.pdf",
            vmax=vmax_cf,
            label=ylabel,
            title=f"Δ {ylabel} (caption - foil) — {in_path.name}",
            y_label="Componente / token slot",
        )

        # Trends (caption & foil absolute values, not deltas)
        plot_caption_foil_trends_pdf(
            summary=summary,
            out_path=subdir / "caption_foil_trends_by_layer.pdf",
            value_col=value_col,
            label=ylabel,
            title_prefix=in_path.name,
        )

        # --- Plot Δ(masked - none) for caption and foil (if computed)
        if vmax_mvn is not None:
            plot_heatmap_fixed_scale(
                delta_df=mvn_caption,
                out_path=subdir / "delta_masked_minus_none_caption.png",
                vmax=vmax_mvn,
                label=ylabel,
                title=f"Δ {ylabel} (masked - {args.none_key}) — caption — {in_path.name}",
                y_label=f"Mask setting (excl. {args.none_key})",
            )
            plot_heatmap_fixed_scale(
                delta_df=mvn_caption,
                out_path=subdir / "delta_masked_minus_none_caption.pdf",
                vmax=vmax_mvn,
                label=ylabel,
                title=f"Δ {ylabel} (masked - {args.none_key}) — caption — {in_path.name}",
                y_label=f"Mask setting (excl. {args.none_key})",
            )
            plot_heatmap_fixed_scale(
                delta_df=mvn_foil,
                out_path=subdir / "delta_masked_minus_none_foil.png",
                vmax=vmax_mvn,
                label=ylabel,
                title=f"Δ {ylabel} (masked - {args.none_key}) — foil — {in_path.name}",
                y_label=f"Mask setting (excl. {args.none_key})",
            )
            plot_heatmap_fixed_scale(
                delta_df=mvn_foil,
                out_path=subdir / "delta_masked_minus_none_foil.pdf",
                vmax=vmax_mvn,
                label=ylabel,
                title=f"Δ {ylabel} (masked - {args.none_key}) — foil — {in_path.name}",
                y_label=f"Mask setting (excl. {args.none_key})",
            )

        # --- Clipping stats per file
        v_cf = _abs_values_from_df(cf) if cf is not None and not cf.empty else np.array([], dtype=float)
        cf_n, cf_clip, cf_pct, cf_max = _clipping_stats_from_vals(v_cf, vmax_cf)

        mvn_n = mvn_clip = 0
        mvn_pct = 0.0
        mvn_max = 0.0
        if vmax_mvn is not None:
            vals = []
            for d in (mvn_caption, mvn_foil):
                if d is not None and not d.empty:
                    v = _abs_values_from_df(d)
                    if v.size:
                        vals.append(v)
            if vals:
                v_all = np.concatenate(vals)
                mvn_n, mvn_clip, mvn_pct, mvn_max = _clipping_stats_from_vals(v_all, vmax_mvn)

        row = {
            "file": in_path.name,
            "subdir": subdir.name,
            "cf_n_points": cf_n,
            "cf_n_clipped": cf_clip,
            "cf_perc_clipped": cf_pct,
            "cf_max_abs_delta": cf_max,
            "mvn_n_points": mvn_n,
            "mvn_n_clipped": mvn_clip,
            "mvn_perc_clipped": mvn_pct,
            "mvn_max_abs_delta": mvn_max,
        }
        report_rows.append(row)

        print(f"[{in_path.name}] Output: {subdir}")
        print(f"[{in_path.name}] CF clipped:  {cf_clip}/{cf_n} ({cf_pct:.2f}%), max|Δ|={cf_max:.6f}")
        if vmax_mvn is not None:
            print(f"[{in_path.name}] MVN clipped: {mvn_clip}/{mvn_n} ({mvn_pct:.2f}%), max|Δ|={mvn_max:.6f}")
        else:
            print(f"[{in_path.name}] MVN clipped: n/a (no baseline '{args.none_key}' or no data)")
        print()

    report_df = pd.DataFrame(report_rows)
    report_path = args.outdir / "clipping_report.csv"
    report_df.to_csv(report_path, index=False)

    print("=== Clipping report (per file) ===")
    print(report_df.to_string(index=False))
    print(f"\nSaved: {report_path}")
    print(f"All results in: {args.outdir.resolve()}")


if __name__ == "__main__":
    main()

